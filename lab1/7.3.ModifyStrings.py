#Upper Case
a = "Hello, World!"
print(a.upper())

#Lower Case
b = "Hello, World!"
print(b.lower())

#Remove Whitespace
c = " Hello, World! "
print(c.strip()) # returns "Hello, World!"

#Replace String
d = "Hello, World!"
print(d.replace("H", "J"))

#Split String
e = "Hello, World!"
print(e.split(",")) # returns ['Hello', ' World!']


